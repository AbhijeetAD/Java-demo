 class MathOperations {

    public static void main(String[] args) {
        int number = 2345;
        int addedNumber = number + 8;
        int quotient = addedNumber / 3;
        int modulus = quotient % 5;
        int finalResult = modulus * 5;

        
        System.out.println("The final result is: " + finalResult);
    }
}
